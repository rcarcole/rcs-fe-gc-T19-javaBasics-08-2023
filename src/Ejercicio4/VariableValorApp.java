package Ejercicio4;

public class VariableValorApp {

	public static void main(String[] args) {
		int N = 5;
		
		int incrementar = N + 77;
		int decrementar = incrementar - 3;
		int duplicar = decrementar * 2;
		System.out.println("Valor inicial de 'N' es: " +N);
		System.out.println("Incrementar 'N' en 3 = " +incrementar);
		System.out.println("Decrementarlo en 3 = " +decrementar);
		System.out.println("Duplicar su valor = " +duplicar);

		
	}

}
